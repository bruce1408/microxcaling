#!/bin/bash
# ==========================================
# MX 量化演示程序编译和运行脚本
# ==========================================
# 用法: ./run_mx_demo.sh
# 
# 编译 mx_demo.cpp 并运行演示程序
# 需要安装 g++ 编译器
# ==========================================

set -e  # 遇到错误立即退出

echo "=== 编译 mx_demo.cpp ==="
g++ mx_demo.cpp -o mx_demo.out -std=c++11

echo ""
echo "=== 编译成功，开始运行 ==="
./mx_demo.out

echo ""
echo "=== 运行完成 ==="